pie = 3.14159
print(int(pie)) # 3 Cuts everything after the decimal point
print(round(pie)) # 3 Rounds to the nearest whole number
print(round(pie, 2)) # 3.14 Rounds to the nearest first decimal place
#f string
print(f"pi is approximately {pie}") #By using f string, we can insert the value of pie into the string directly